			'TIMESTAMP WITH TIME ZONE'  => FSQL_TYPE_TIMESTAMP_WITH_TZ,
			'TIMESTAMP WITHOUT TIME ZONE' => FSQL_TYPE_TIMESTAMP,
		);
	}
}

?>